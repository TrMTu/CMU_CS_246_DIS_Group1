    private void btnBangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBangActionPerformed
        // TODO add your handling code here:
        no2=Double.parseDouble(jtxtDisplay.getText());
        switch(operator){
            case '+':
            total = no1 + no2;
            break;
            case '-':
            total = no1 - no2;
            break;
            case '*':
            total = no1 * no2;
            break;
            case '/':
            total = no1 / no2;
            break;
        }
        jtxtDisplay.setText(Double.toString(total));
        no1=0.0;
        no2=0.0;
    }//GEN-LAST:event_btnBangActionPerformed

